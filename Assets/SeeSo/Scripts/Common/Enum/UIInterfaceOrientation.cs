/// <summary>
///   Constants that specify the orientation of the app's user interface.
/// </summary>
/// <b>Note</b>
/// - <b>UNKNOWN</b> The orientation of the device is unknown.
/// - <b>PORTRAIT</b> The device is in portrait mode, with the device upright and the Home button on the bottom.
/// - <b>PORTRAIT_UPSIDE_DOWN</b> The device is in portrait mode but is upside down, with the device upright and the Home button at the top.
/// - <b>LANDSCAPE_RIGHT</b> The device is in landscape mode, with the device upright and the Home button on the right.
/// - <b>LANDSCAPE_LEFT</b> The device is in landscape mode, with the device upright and the Home button on the left.
public enum UIInterfaceOrientation
{
    UNKNOWN = 0,
    PORTRAIT = 1,
    PORTRAIT_UPSIDE_DOWN = 2,
    LANDSCAPE_RIGHT = 3,
    LANDSCAPE_LEFT = 4
}

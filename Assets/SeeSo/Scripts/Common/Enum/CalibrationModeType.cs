/// <summary>
/// The enum Calibration mode type.
/// </summary>
/// <b>Note</b>
/// - <b>DEFAULT</b> means that Represent FIVE_POINT.
/// - <b>ONE_POINT</b> means that one-point calibration mode.
/// - <b>FIVE_POINT</b> means that five-point calibration mode.
/// - <b>SIX_POINT</b> means that six-point calibration mode.

public enum CalibrationModeType
{
    DEFAULT = 0,
    ONE_POINT = 1,
    FIVE_POINT = 5,
    SIX_POINT = 6
}
